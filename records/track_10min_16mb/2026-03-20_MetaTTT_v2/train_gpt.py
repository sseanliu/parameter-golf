def _get_ttt_param_names(raw_model: GPT) -> list[str]:
    """Identify TTT params: MLP layers of last 1/4 blocks (last 3 of 11 = blocks 8,9,10)."""
    num_blocks = len(raw_model.blocks)
    suffix_start = num_blocks - num_blocks // 4
    return [name for name, _ in raw_model.named_parameters()
            if any(f'blocks.{i}.' in name and '.mlp.' in name for i in range(suffix_start, num_blocks))]


def _build_ttt_param_dict(raw_model: GPT, ttt_param_names: list[str]) -> dict[str, nn.Parameter]:
    """Build a name->param dict for fast lookup (avoid repeated dict() calls)."""
    all_params = dict(raw_model.named_parameters())
    return {name: all_params[name] for name in ttt_param_names}


def _reset_rotary_caches(model: GPT) -> None:
    """Reset all Rotary caches in the model."""
    for block in model.blocks:
        block.attn.rotary.reset_cache()


def _create_fresh_gpt(args: Hyperparameters) -> GPT:
    """Create a fresh GPT instance with proper dtype setup."""
    m = GPT(
        vocab_size=args.vocab_size, num_layers=args.num_layers, model_dim=args.model_dim,
        num_heads=args.num_heads, num_kv_heads=args.num_kv_heads, mlp_mult=args.mlp_mult,
        tie_embeddings=args.tie_embeddings, tied_embed_init_std=args.tied_embed_init_std,
        logit_softcap=args.logit_softcap, rope_base=args.rope_base, qk_gain_init=args.qk_gain_init,
        bigram_vocab_size=args.bigram_vocab_size, bigram_dim=args.bigram_dim,
    )
    return m


# -----------------------------
# TTT EVAL (OUTSIDE inference_mode, NO compile)
# -----------------------------

def eval_val_ttt(
    args: Hyperparameters, state_dict: dict[str, Tensor],
    rank: int, world_size: int, device: torch.device,
    val_tokens: Tensor, base_bytes_lut: Tensor,
    has_leading_space_lut: Tensor, is_boundary_token_lut: Tensor,
) -> tuple[float, float]:
    """
    Evaluate with test-time training. Creates a FRESH model copy from state_dict.
    Must be called OUTSIDE any inference_mode context. Does NOT use torch.compile.
    """
    seq_len = args.train_seq_len
    stride = seq_len  # TTT uses non-overlapping windows for speed (no stride=64)
    total = int((val_tokens.numel() - 1) * args.ttt_val_frac)

    # Create fresh model (not from quantize roundtrip) -- copy weights in
    ttt_model = _create_fresh_gpt(args).to(device).bfloat16()
    for m in ttt_model.modules():
        if isinstance(m, CastedLinear):
            m.float()
    restore_low_dim_params_to_fp32(ttt_model)
    ttt_model.load_state_dict(state_dict, strict=True)
    _reset_rotary_caches(ttt_model)

    # Identify TTT params
    ttt_param_names = _get_ttt_param_names(ttt_model)
    ttt_params = _build_ttt_param_dict(ttt_model, ttt_param_names)

    # Save original state for reset between documents
    orig_state = {name: p.data.clone() for name, p in ttt_params.items()}

    loss_sum = torch.zeros((), device=device, dtype=torch.float64)
    tok_count = torch.zeros((), device=device, dtype=torch.float64)
    byte_count = torch.zeros((), device=device, dtype=torch.float64)

    # Build sliding windows
    windows: list[tuple[int, int]] = []
    p = 0
    while p + seq_len <= total:
        s = 0 if p == 0 else (seq_len - stride)
        windows.append((p, s))
        p += stride

    # Process each window: score then train
    for wi, (w_start, score_from) in enumerate(windows):
        x = val_tokens[w_start:w_start + seq_len].unsqueeze(0).to(device=device, dtype=torch.int64)
        y = val_tokens[w_start + 1:w_start + seq_len + 1].unsqueeze(0).to(device=device, dtype=torch.int64)

        # Step A: Score the chunk (no_grad for accumulating BPB)
        ttt_model.eval()
        with torch.no_grad(), torch.autocast(device_type="cuda", dtype=torch.bfloat16):
            logits = ttt_model.forward_logits(x)
        sl = logits[0, score_from:]
        st = y[0, score_from:]
        loss = F.cross_entropy(sl.float(), st, reduction="sum")
        loss_sum += loss.to(torch.float64)
        n = st.numel()
        tok_count += n
        prev = x[0, score_from:score_from + n]
        tb = base_bytes_lut[st].to(torch.int16)
        tb += (has_leading_space_lut[st] & ~is_boundary_token_lut[prev]).to(torch.int16)
        byte_count += tb.to(torch.float64).sum()

        # Step B: Train on the chunk (1 SGD step on TTT params)
        if args.ttt_enabled and wi + 1 < len(windows):
            ttt_model.train()
            for _ in range(args.ttt_steps_per_chunk):
                with torch.autocast(device_type="cuda", dtype=torch.bfloat16):
                    train_loss = ttt_model(x, y)
                train_loss.backward()
                with torch.no_grad():
                    for name, param in ttt_params.items():
                        if param.grad is not None:
                            param.data -= args.ttt_lr * param.grad
                ttt_model.zero_grad(set_to_none=True)

    if dist.is_available() and dist.is_initialized():
        dist.all_reduce(loss_sum, op=dist.ReduceOp.SUM)
        dist.all_reduce(tok_count, op=dist.ReduceOp.SUM)
        dist.all_reduce(byte_count, op=dist.ReduceOp.SUM)

    val_loss = (loss_sum / tok_count).item()
    val_bpb = val_loss / math.log(2.0) * (tok_count.item() / byte_count.item())
    # Clean up
    del ttt_model
    torch.cuda.empty_cache()
    return val_loss, val_bpb


